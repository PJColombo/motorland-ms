    public void setComponentes(TCliente t){
    	jTextFieldDni.setText(t.getDni());
        jTextFieldDomicilio.setText(t.getDomicilio());
        jTextFieldIdCliente.setText(Integer.toString(t.getId()));
        jTextFieldNombre.setText(t.getNombre());
        jTextFieldTarjeta.setText(Long.toString(t.getTarjeta()));
        jCheckBoxActivo.setSelected(t.getActivo());
        jCheckBoxDeshabilitado.setSelected(!t.getActivo());
    }
    
    private void limpiarComponentes(){
    	jTextFieldDni.setText("");
        jTextFieldDomicilio.setText("");
        jTextFieldIdCliente.setText("");
        jTextFieldLista.setText("");
        jTextFieldNombre.setText("");
        jTextFieldTarjeta.setText("");
        jCheckBoxActivo.setSelected(false);
        jCheckBoxDeshabilitado.setSelected(false);
    }

