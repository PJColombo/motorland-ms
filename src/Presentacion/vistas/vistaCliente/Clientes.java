    private void jButtonAnadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAnadirActionPerformed
        // TODO add your handling code here:
    	if(jTextFieldDni.getText().isEmpty() || jTextFieldDomicilio.getText().isEmpty() || jTextFieldNombre.getText().isEmpty() || jTextFieldTarjeta.getText().isEmpty() ){
   		 	JOptionPane.showMessageDialog(null,"Fallo al dar de alta un cliente, faltan campos por rellenar");
	   	}
	   	else{
	   		TCliente t = new TCliente();
	   		t.setNombre(jTextFieldNombre.getText());
	   		t.setDni(jTextFieldDni.getText());
	   		t.setDomicilio(jTextFieldDomicilio.getText());
	   		t.setTarjeta(Integer.parseInt(jTextFieldTarjeta.getText()));
	   		t.setActivo(true);
	   		Controller.getInstance().run(ListaComandos.NEGOCIOALTACLIENTE, t);
	   		limpiarComponentes();
   		}
   	}

    private void jButtonBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBorrarActionPerformed
        // TODO add your handling code here:
    	if(jTextFieldIdCliente.getText().isEmpty()){
    		JOptionPane.showMessageDialog(null,"Fallo al dar de baja, rellena el campo idCliente");
    	}

    	else{
	   		int id =Integer.parseInt(jTextFieldIdCliente.getText());
	    	Controller.getInstance().run(ListaComandos.NEGOCIOBAJACLIENTE, id);
	    	limpiarComponentes();

    	}
    }//GEN-LAST:event_jButtonBorrarActionPerformed

    private void jButtonModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonModificarActionPerformed
        // TODO add your handling code here:
    	if(jTextFieldDni.getText().isEmpty() || jTextFieldDomicilio.getText().isEmpty() || jTextFieldNombre.getText().isEmpty() || jTextFieldTarjeta.getText().isEmpty() ){
   		 	JOptionPane.showMessageDialog(null,"Fallo al modificar cliente, faltan campos por rellenar");
	   	}

    	else{
	    	TCliente t = new TCliente();
	    	t.setId(Integer.parseInt(jTextFieldIdCliente.getText()));
	    	t.setNombre(jTextFieldNombre.getText());
	   		t.setDni(jTextFieldDni.getText());
	   		t.setDomicilio(jTextFieldDomicilio.getText());
	   		t.setTarjeta(Integer.parseInt(jTextFieldTarjeta.getText()));
	   		t.setActivo(true);
	   		
	    	Controller.getInstance().run(ListaComandos.NEGOCIOMODIFICARCLIENTE, t);
	    	limpiarComponentes();

    	}
    }//GEN-LAST:event_jButtonModificarActionPerformed

